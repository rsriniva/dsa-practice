intArray = [1, 2, 3, 4, 5]
stringArray = ["one", "two", "three", "four", "five", "six", "seven", "eight"]
charArray = ['a', 'b', 'c', 'd', 'e']

print('######  Constant time complexity O(1) #######')
print("First element of int array = ", intArray[0])
print("2nd element of string array = ", stringArray[1])
print("Last element of char array = ", charArray[len(charArray) - 1])
